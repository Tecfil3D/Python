# Diccionarios

diccionario = {'1': 'Marcos', '2': 'Luis', '3': 'Oscar', 4: 'Miguel'}    # se pueden añadir infinitamente mas datos
print(diccionario['3'])    # imprime la información alojada en la clave que corresponde a ['2']
print(diccionario[4])
# Imprime las claves del diccionario

print('Las claves del diccionario son:', diccionario.keys())

# Imprime los valores del diccionario

print('Los valores del diccionario son:', diccionario.values())

# Imprime el diccionario completo

print('El diccionario completo:', diccionario)
